var name = `Vishal`;
var fullName = `${name} Dubey`;

console.log(fullName);
//return:- Vishal Dubey

console.log(fullName.charAt(0));
//return:- V

console.log(fullName.endsWith("y"));
//return:- true

console.log(fullName.includes("Vishal"));
//return :- true

console.log(fullName.toLocaleLowerCase());
// return:- vishal dubey

console.log(fullName.toLocaleUpperCase());
// return:- VISHAL DUBEY
